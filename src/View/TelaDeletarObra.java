/**
     * Método que remove uma Obra.
     * @param evt ActionEvent, Ação do botão sendo clicado
     */
    private void DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarActionPerformed
        if (Tabela.getSelectedRow()>=0) {
            String nome = (String) Tabela.getValueAt(Tabela.getSelectedRow(),0);
            String assunto = (String) Tabela.getValueAt(Tabela.getSelectedRow(),2);
            String tipo = (String) Tabela.getValueAt(Tabela.getSelectedRow(),6);
            for (Obra o : array) {
                if(o.getNome().equals(nome) && o.getAssunto().equals(assunto) && o.getTipo().equals(tipo)){
                    co.Remover(o);
                    array.remove(o);
                    dtm.removeRow(Tabela.getSelectedRow());
                    break;
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Selecione uma Obra","Aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_DeletarActionPerformed
/**
     * Método que cria o comentario dos valores da tabela de Administradores
     * @param evt MouseEvent, ação de mover o mouse pela tabela
     */
    private void TabelaMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaMouseMoved
        Point p = evt.getPoint(); 
        int row = Tabela.rowAtPoint(p);
        int column = Tabela.columnAtPoint(p);
        Tabela.setToolTipText(String.valueOf(dtm.getValueAt(row,column)));
    }//GEN-LAST:event_TabelaMouseMoved

 

