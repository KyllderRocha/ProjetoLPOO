/**
     * M√©todo que cria um Emprestimo.
     * @param evt ActionEvent, A√ß√£o do bot√£o sendo clicado
     */
    private void AdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdicionarActionPerformed
        if (TabelaObra.getSelectedRow()>=0 && TabelaUsuario.getSelectedRow()>=0) {
            String nome = (String) TabelaObra.getValueAt(TabelaObra.getSelectedRow(),0);
            String assunto = (String) TabelaObra.getValueAt(TabelaObra.getSelectedRow(),2);
            String tipo = (String) TabelaObra.getValueAt(TabelaObra.getSelectedRow(),6);
            int copias=0;
            for (Obra o : arrayO) {
                if(o.getNome().equals(nome) && o.getAssunto().equals(assunto) && o.getTipo().equals(tipo)){
                    copias=o.getCopias();
                    break;
                }
            }
            
            if (ce.CopiaDisponivel(nome, copias)) {
                Calendar dtPrevista = Calendar.getInstance();
                String cpf = (String) TabelaUsuario.getValueAt(TabelaUsuario.getSelectedRow(),1);
                for (Usuarios aux : arrayU) {
                if(aux.getCPF().equals(cpf)){
                    if (aux.getDivida()>0) {
                       ce.TelaDivida(this,true,aux.getDivida(), aux);
                    }
                    break;
                }
            }
                
                arrayU=cobre.Listar();
                try {
            for (Usuarios aux : arrayU) {
                if(aux.getCPF().equals(cpf)){
                    if (aux.getDivida()==0) {
                    	if(Calendar.getInstance().compareTo(dtPrevista)>0){
                    		throw new DataPrevistaInvalida("A data atual n„o pode ser maior que a data prevista!");
                    	}
                        dtPrevista.add(Calendar.DATE,aux.getDias());
                        Emprestimo e = new Emprestimo(aux.getNome(), Admin, (String)TabelaObra.getValueAt(TabelaObra.getSelectedRow(),0), dtPrevista, null, Calendar.getInstance());
                        ce.Adcionar(e);
                        dispose();
                    }
                }
            }
                } catch (DataPrevistaInvalida dpi) {
					System.out.println(dpi.getMessage());			}
            }else{
                JOptionPane.showMessageDialog(null,"C√≥pias indispon√≠veis","Erro",JOptionPane.WARNING_MESSAGE);
            }
            
        }else{
            JOptionPane.showMessageDialog(null,"Selecione ums Obra e um Usu√°rio","Aviso",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AdicionarActionPerformed
